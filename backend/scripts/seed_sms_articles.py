async def find_old_sms_article(session) -> Article | None:
    """Ищет старую монолитную статью по slug-вариантам или по title."""
    for old_slug in OLD_SMS_SLUG_VARIANTS:
        r = await session.execute(select(Article).where(Article.slug == old_slug))
        a = r.scalar_one_or_none()
        if a:
            return a
    # Fallback: поиск по title containing
    r = await session.execute(
        select(Article).where(
            Article.title.ilike("%не приходит sms%"),
            Article.title.ilike("%регистрации%или%восстановления%"),
        )
    )
    return r.scalar_one_or_none()


def _slugify_simple(text: str) -> str:
    """Мини-версия slugify из backend/knowledge/router.py для локального использования."""
    translit_map = {
        "а": "a", "б": "b", "в": "v", "г": "g", "д": "d", "е": "e",
        "ё": "yo", "ж": "zh", "з": "z", "и": "i", "й": "y", "к": "k",
        "л": "l", "м": "m", "н": "n", "о": "o", "п": "p", "р": "r",
        "с": "s", "т": "t", "у": "u", "ф": "f", "х": "kh", "ц": "ts",
        "ч": "ch", "ш": "sh", "щ": "shch", "ъ": "", "ы": "y", "ь": "",
        "э": "e", "ю": "yu", "я": "ya",
    }
    import re
    result = []
    for ch in text.lower():
        result.append(translit_map.get(ch, ch))
    s = "".join(result)
    s = re.sub(r"[^a-z0-9]+", "-", s)
    return s.strip("-")


async def run(apply: bool) -> None:
    mode = "APPLY" if apply else "DRY-RUN"
    logger.info("=== MUST-2: SMS articles decomposition (%s) ===", mode)

    async with async_session_factory() as session:
        # Найти автора (первого admin)
        from backend.auth.models import User, UserRole
        admin_r = await session.execute(select(User).where(User.role == UserRole.ADMIN).limit(1))
        admin = admin_r.scalar_one_or_none()
        if not admin:
            logger.error("Не найден admin для создания статей")
            return
        author_id = admin.id

        # Найти старую статью
        old_article = await find_old_sms_article(session)
        if old_article:
            logger.info("Найдена старая статья: %r (slug: %s)", old_article.title, old_article.slug)
        else:
            logger.info("Старая монолитная статья не найдена — все статьи будут новыми")

        created = 0
        updated = 0

        for i, spec in enumerate(ARTICLES, 1):
            new_slug = _slugify_simple(spec["title"])

            # Для первой статьи (регистрация) — переиспользуем старую если она есть
            target: Article | None = None
            if i == 1 and old_article:
                target = old_article
            else:
                # Ищем по новому slug или по title
                r = await session.execute(select(Article).where(Article.slug == new_slug))
                target = r.scalar_one_or_none()

            if target:
                # Update existing
                logger.info("[%d/6] UPDATE: %r", i, spec["title"])
                # Сохраняем старый slug в aliases если меняем slug
                if target.slug != new_slug:
                    existing_aliases = list(target.slug_aliases or [])
                    if target.slug not in existing_aliases:
                        existing_aliases.append(target.slug)
                    target.slug_aliases = existing_aliases
                    target.slug = new_slug

                target.title = spec["title"]
                target.category = spec["category"]
                target.article_type = spec["article_type"]
                target.tags = spec["tags"]
                target.synonyms = spec["synonyms"]
                target.content = spec["content"]

                if apply:
                    session.add(target)
                updated += 1
            else:
                # Create new
                logger.info("[%d/6] CREATE: %r", i, spec["title"])
                new_article = Article(
                    id=uuid.uuid4(),
                    title=spec["title"],
                    slug=new_slug,
                    category=spec["category"],
                    article_type=spec["article_type"],
                    content=spec["content"],
                    tags=spec["tags"],
                    synonyms=spec["synonyms"],
                    author_id=author_id,
                )
                if apply:
                    session.add(new_article)
                created += 1

        if apply:
            await session.commit()
            logger.info("✅ Применено: создано %d, обновлено %d", created, updated)
        else:
            logger.info("DRY-RUN: будет создано %d, обновлено %d. Используйте --apply", created, updated)


def main() -> None:
    parser = argparse.ArgumentParser(description="Декомпозиция SMS-статей (MUST-2)")
    parser.add_argument("--apply", action="store_true", help="Сохранить изменения в БД")
    args = parser.parse_args()
    asyncio.run(run(apply=args.apply))


if __name__ == "__main__":
    main()
