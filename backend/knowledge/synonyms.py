"""Словарь синонимов для query expansion перед FTS.

Пользователи пишут разговорно: "смс не приходит", "вылетает прога".
FTS Russian stemmer справляется со словоформами, но не с жаргоном и
альтернативными формулировками. Прогоняем запрос через словарь
и расширяем термами, которые точно встречаются в заголовках/synonyms статей.

Принцип: если в запросе встретился ключ — добавляем к запросу все значения.
Результат подаётся в plainto_tsquery.
"""
from __future__ import annotations

import re
from typing import Iterable

# Ключ — токен из пользовательского запроса (lowercased, без знаков препинания).
# Значения — дополнительные термы, добавляемые к FTS запросу.
_SYNONYMS: dict[str, tuple[str, ...]] = {
    # SMS-коды: самая частая проблема (371 заявка = 10.7% потока)
    "смс": ("SMS", "код", "сообщение"),
    "sms": ("смс", "код", "сообщение"),
    "код": ("SMS", "смс", "sms-код"),
    "кода": ("SMS", "смс", "код"),
    "приходит": ("получить", "отправить", "пришёл", "пришел"),
    "пришёл": ("получить", "приходит", "пришел"),
    "пришел": ("получить", "приходит", "пришёл"),
    # Регистрация
    "регистрация": ("зарегистрироваться", "создать аккаунт", "новый пользователь"),
    "зарегистрироваться": ("регистрация", "создать аккаунт"),
    "регистрации": ("регистрация", "зарегистрироваться"),
    # Вход / логин / пароль
    "вход": ("войти", "логин", "зайти", "авторизация"),
    "войти": ("вход", "логин", "зайти"),
    "логин": ("вход", "войти", "авторизация"),
    "зайти": ("вход", "войти"),
    "пароль": ("password", "восстановить", "забыл", "забыла"),
    "пароля": ("пароль", "восстановить"),
    "забыл": ("пароль", "восстановить", "не помню"),
    "забыла": ("пароль", "восстановить", "не помню"),
    "восстановить": ("пароль", "сбросить", "забыл"),
    # Приложение / мобилка
    "приложение": ("app", "прога", "мобильное"),
    "прога": ("приложение", "app"),
    "вылетает": ("крашится", "закрывается", "не работает"),
    "крашится": ("вылетает", "не работает"),
    "тормозит": ("медленно", "зависает", "лагает"),
    "зависает": ("тормозит", "не отвечает"),
    # Пропуска
    "пропуск": ("доступ", "разрешение", "qr"),
    "пропуска": ("пропуск", "доступ"),
    "пропусков": ("пропуск", "доступ"),
    "qr": ("QR-код", "qr-код", "код"),
    # Шлагбаумы / камеры / распознавание
    "шлагбаум": ("ворота", "барьер", "въезд"),
    "шлагбаума": ("шлагбаум", "ворота"),
    "ворота": ("шлагбаум"),
    "камера": ("распознавание", "номер"),
    "камеры": ("камера", "распознавание"),
    "номер": ("госномер", "распознавание", "авто"),
    "распознавание": ("камера", "номер"),
    "распознаёт": ("распознавание", "считывает"),
    "распознает": ("распознавание", "считывает"),
    "считывает": ("распознавание", "распознаёт"),
    # Уведомления
    "уведомления": ("push", "оповещения", "нотификации"),
    "уведомление": ("push", "оповещение"),
    "push": ("уведомления", "оповещения"),
    "пуш": ("push", "уведомления"),
    # Разное
    "не": (),  # не добавляем синонимы для "не", это носит отрицание
    "нет": ("не получаю", "отсутствует", "нету"),
    "проблема": ("не работает", "ошибка"),
    "ошибка": ("не работает", "проблема", "баг"),
}


def _tokenize(query: str) -> list[str]:
    """Разбивает запрос на слова (lowercase, убирая пунктуацию)."""
    # Оставляем буквы/цифры/дефис, остальное — разделитель
    return [t for t in re.split(r"[^\w\-]+", query.lower()) if t]


def expand_query(query: str, *, max_extra_terms: int = 8) -> str:
    """Расширяет пользовательский запрос синонимами для FTS.

    Принципы:
    - Исходный запрос сохраняется
    - Для каждого токена добавляются релевантные синонимы из словаря
    - Дубликаты удаляются
    - Ограничиваем max_extra_terms, чтобы tsquery не разбух

    Args:
        query: Пользовательский запрос ("не приходит смс")
        max_extra_terms: Макс. число добавленных термов

    Returns:
        Расширенный запрос ("не приходит смс SMS код сообщение")
    """
    if not query or not query.strip():
        return query

    tokens = _tokenize(query)
    extra: list[str] = []
    seen: set[str] = {t for t in tokens}

    for token in tokens:
        synonyms: Iterable[str] = _SYNONYMS.get(token, ())
        for syn in synonyms:
            syn_lower = syn.lower()
            if syn_lower in seen:
                continue
            # Проверяем каждое слово в синониме
            for word in _tokenize(syn):
                if word not in seen:
                    extra.append(word)
                    seen.add(word)
                    if len(extra) >= max_extra_terms:
                        break
            if len(extra) >= max_extra_terms:
                break
        if len(extra) >= max_extra_terms:
            break

    if not extra:
        return query

    return query + " " + " ".join(extra)
